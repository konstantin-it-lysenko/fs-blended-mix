const num = 5;
let result = "";
switch (num) {
  case 1:
    result = "зима";
    break;
  case 2:
    result = "весна";
    break;
  case 3:
    result = "літо";
    break;
  case 4:
    result = "осінь";
    break;
  default:
    result = "Такої пори року немає";
}
console.log("💖 ~ result:", result);
